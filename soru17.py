#17-Kenar uzunluklarını kullanıcıdan alınan  Dikdörtgenin Alanı ve Çevresini Bul

f = int(input('Birinci kenarın uzunluğunu giriniz: '))
s = int(input('İkinci kenarın uzunluğunu giriniz: '))

print(f'Dikdörtgeninizin alanı: {f*s}')#f ile kullanıcıdan alının verinin okunmasını sağladık
print(f'Dikdörtgeninizin çevresi: {(f+s)*2}')#f ile kullanıcıdan alının verinin okunmasını sağladık
